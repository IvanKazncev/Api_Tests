package Api;

import java.util.Date;

public class SuccesCreate {
    private String name;
    private String job;
    private String id;
    private Date createdAt;
    private Integer status;

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }

    public String getId() {
        return id;
    }

    public Date getCreatedAt() {
        return createdAt;
    }
    public Integer getStatus() {
        return status;
    }

}
